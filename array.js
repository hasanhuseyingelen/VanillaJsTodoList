/**
 * 
 * @param {*} target 
 * @param {*} value 
 */
export function addArray(target, value) {
    target.push(value);
}


/**
 * 
 * @param {*} array Array List
 * @param {*} value Find value 
 */
export function findInArray(array, value) {
    return array.includes(value);
}

